# 6. 두 정수를 입력 받은 후 산술 연산 /,%,//,** 4개를 수행해 결과를 출력하는
# 프로그램을 작성하시오.

num1 = input('Enter First number: ')
num2 = input('Enter Second number: ')

print('12 / 5 ==> ', int(num1)/int(num2))
print('12 % 5 ==> ', int(num1) % int(num2))
print('12 // 5 ==> ', int(num1)//int(num2))
print('12 ** 5 ==> ', int(num1)**int(num2))
