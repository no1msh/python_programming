num1, num2 = input('실수 두개 입력 >> ').split()
# .split() 메소드를 통하여 표준입력 하나로 두개의 변수 이상을 저장할 수 있음
print(num1, '>', num2, '결과:', num1 > num2)
print(num1, '>=', num2, '결과:', num1 >= num2)
print(num1, '<', num2, '결과:', num1 < num2)
print(num1, '<=', num2, '결과:', num1 <= num2)
print(num1, '==', num2, '결과:', num1 == num2)
print(num1, '!=', num2, '결과:', num1 != num2)
